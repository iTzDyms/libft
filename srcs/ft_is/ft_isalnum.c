int	ft_isalnum(int c)
{
	int	ret;

	ret = 0;
	if ((c >= 'A' && c <= 'Z')\
		|| (c >= 'a' && c <= 'z') || (c >= '0' && c <= '9'))
		ret++;
	return (ret);
}


#include <unistd.h>
#include "libft.h"

void	ft_putnbr(int n)
{
	if (n == -2147483648)
		{
			write(1, "-2147483648", 11);
			return ;
		}
	if (n < 0)
		{
			n = - n;
			ft_putchar('-');
		}
	if (n / 10 > 0)
		ft_putnbr(n / 10);
	ft_putchar((n % 10) + '0');
}

int	ft_isspace(int c)
{
	int	ret;

	ret = 0;
	if ((c == '\f') || (c == ' ') || (c == '\n')
		|| (c == '\t') || (c == '\r') || (c == '\v'))
		ret++;
	return (ret);
}
